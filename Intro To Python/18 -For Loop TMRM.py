#The For Loop will set i as the range of the first number (0) to the second (5)
print("\n")
for i in range (0,5):
   print(i)

#Prints out the number 0,1,2,3,4
#If there is only one number in the range then it will set the range from zero to that number (5)
print("\n")
for x in range(5):
    print(x)
#Prints out 3,4,5
print("\n")
for y in range(3,6):
    print(y)
#Prints out, 3,5,7
#If there are three numbers in the range the third increments the count, in this case every second number(2)
print("\n")
for z in range(3, 8, 2):
    print(z)

